package LN;

public class Jugador {

	private String nombre;
	private int numJug;
	
	public Jugador(String nombre, int numJug){
		nombre = this.nombre;
		numJug = this.numJug;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public int getNumJug() {
		return numJug;
	}

	public void setNumJug(int numJug) {
		this.numJug = numJug;
	}
	
	
}
